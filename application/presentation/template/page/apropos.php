<h1><img src="resource/favicon.png"/> Contresort le framework sans magie (ou presque)</h1>
	<h2>Philosophie</h2>
	<p>Contrairement à la plupart des framework et autres "content management framework" qui essayent de répondre à tous les besoins possibles, Contresort se concentre sur un point : la vitesse de globale de production d'applications. Il faut comprendre par "vitesse globale" les coûts en temps suivant :
	<ul>
		<li>Apprentissage</li>
		<li>Conception et arbitrage</li>
		<li>Installation</li>
		<li>Développement</li>
		<li>Déploiement</li>
		<li>Maintenance</li>
		<li>Abandon de Contresort pour un autre framework (T_T)</li>
	</ul>
	</p>
	<p>Pour atteindre ces objectifs, les choix de développement de Contresort sont guidés par ces idées :
	<ul>
		<li>Etre le plus proche possible du PHP natif</li>
		<li>Proscrire toute magie basée sur l'utilisation de conventions arbitraires</li>
		<li>L'architecture doit être intuitive et compréhensible</li>
		<li>Les développements réalisés sur Contresort ne doivent pas être dépendants de l'architecture framework</li>
		<li>Le framework doit proposer des solutions mais ne pas en imposer</li>
		<li><a href="http://fr.wikipedia.org/wiki/Principe_KISS">KISS</a> et <a href="http://fr.wikipedia.org/wiki/YAGNI">YAGNI</a></li>
	</ul>
	</p>
	<p>
		Cette philosophie a été choisie pour les raisons (discutables) suivantes : <ul>
		<li>Un développeur débutant sur un framework produit du code de qualité inférieure à ses compétences PHP</li>
		<li>Plus le framework se veut complet, plus les développeurs restent longtemps débutants</li>
		<li>Les schémas d'architecture et le langage PHP évoluent plus vitent que les frameworks</li>
		<li>Idéalement un framework devrait faire progresser un développeur, mais surtout pas le "politiser"</li>
		<li>(Pour être totalement efficace sur un framework, un développeur ne devrait pas avoir a suivre des formations spécifiques payantes ^_^)</li>
	</ul>
	</p>
	<h2>Pourquoi utiliser Contresort</h2>
	<p>
		Contresort s'adresse aux développeurs voulant livrer des produits rapidement et ne prenant pas particulièrement de plaisir dans la conception d'une architecture logicielle. De plus de par sa philosophie d'ouverture, vous n'aurez pas à vous soucier d'avoir fait les bons arbitrages techniques du premier coup. En effet si jamais vous réalisez que telle ou telle brique ne répond pas à vos besoins, vous pourrez facilement concevoir votre propre solution ou bien en intégrer une réalisée par la communauté. Voici une liste de goûts pouvant vous inciter à utiliser Contresort : <ul>
		<li>Vous privilégiez la fonctionnalité à la beauté d'une architecture</li>
		<li>Vous pensez qu'il est rarement possible de faire le bon choix technique du premier coup</li>
		<li>Vous préférez régulièrement refactoriser une code simple plutôt que de concevoir du premier coup une architecture complexe répondant à tout</li>
		<li>Vous pensez que "faire" est plus interessant que "savoir"</li>
		<li>Vous trouvez que les débats d'architecture et de conception ont trop tendance à ressembler à des guerres de religions</li>
	</ul>
	</p>
	<h2>Pourquoi ne pas utiliser Contresort</h2>
	<p>
		Contresort répond à des besoins spécifiques mais n'est bien sûr pas adapté à tout les développeurs. C'est pour cette raison qu'il a été conçu pour pouvoir êtrr abandonné facilement pour une autre solution. Cependant afin de vous faire gagner du temps voici quelques raisons pour lesquelles vous ne devriez pas choisir Contresort : <ul>
		<li>Vous préfèrez configurer plutôt que coder</li>
		<li>En cas de doute, vous choisirez <a href="http://fr.wikipedia.org/wiki/SOLID_(informatique)">SOLID</a> plutôt que <a href="http://fr.wikipedia.org/wiki/Principe_KISS">KISS</a></li>
		<li>Vous concevez votre code en fonction de design pattern plutôt qu'utilisez ces derniers en fonction de vos besoins immédiats</li>
		<li>Vous prenez plus de plaisir à maitriser des produits complexes qu'à livrer des fonctionnalités</li>
	</ul>
	</p>
