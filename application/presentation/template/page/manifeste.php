		<h2>Pourquoi éviter la magie</h2>
		<p>
			La magie est la constatation d'effets pour lesquels une personne non initiée est incapable d'en deviner les causes. En outre la magie se veut réservée à des individus ayant longtemps étudiés
			ses mécanismes et lois plus ou moins tordus. Contresort va à l'encontre de tout cela et nous voulons que n'importe qui puisse produire rapidement des applications fantastiques sans avoir à acquérir une somme considérable de connaissances.
		</p>

		<h2>Pourquoi Contresort est conçu pour pouvoir être abandonné facilement</h2>
		<p>Parce que nous souhaitons que la seule et unique raison qui vous pousse à garder Contresort soit que ce dernier vous donne entière satisfaction. Nous voulons absolument éviter que vous soyez prisonnier d'une technologie qui après coup ne vous conviendrait pas totalement.</p>

		<h2>KISS avant tout</h2>
		<p>Tous les choix d'implémentation de Contresort sont réalisés avec pour objectif de rester le plus simple possible. La simplicité offre une courbe d'apprentissage rapide, une maintenance aisée et des possibilités d'évolutions non bridés par des contraintes techniques. Nous voulons que les développeurs passent plus de temps à réfléchir à comment améliorer leurs produits plutôt qu'à se demander comment contourner des choix que nous aurions pu faire et qui ne leurs conviendraient pas.
		</p>

		<h2>Le développeur est le chef d'orchestre, pas le framework</h2>
		<p>
			Contresort est conçu comme une formation instrumentale (rock ^_^) minimaliste mais efficace. Une guitare, une basse et un kit de precussions. Cependant si vous souhaitez modifier cette formation et la faire grandir, nous voulons que vous puissiez le faire facilement. C'est pour cette raison que Contresort vous laisse un maximum de liberté et peut paraitre limité en proposant un cadre de développement extrémement réduit.
		</p>

		<h2>Pas de bibliothèques externes</h2>
		<p>Contresort n'utilise pas de bibliothèques externes ! Nous avons fait ce choix non pas parce que nous aimons réinventer la roue ou que nous pensons mieux faire que les autres, mais uniquement parce que nous ne voulons pas imposer nos choix ou préférences. Cependant le mécanisme d'extensions a été conçu pour que vous puissiez intégrer vos outils favoris facilement à Contresort.
		</p>



